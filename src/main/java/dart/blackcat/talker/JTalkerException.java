package dart.blackcat.talker;

public class JTalkerException extends Exception {

	private static final long serialVersionUID = 2390533587135084843L;

	public JTalkerException() {
	}

	public JTalkerException(String message) {
		super(message);
	}

	public JTalkerException(Throwable cause) {
		super(cause);
	}

	public JTalkerException(String message, Throwable cause) {
		super(message, cause);
	}

}
