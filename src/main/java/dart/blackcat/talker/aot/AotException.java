package dart.blackcat.talker.aot;

import dart.blackcat.talker.JTalkerException;

public class AotException extends JTalkerException {

	private static final long serialVersionUID = -6895800877624511881L;

	public AotException() {
		super();
	}

	public AotException(String message, Throwable cause) {
		super(message, cause);
	}

	public AotException(String message) {
		super(message);
	}

	public AotException(Throwable cause) {
		super(cause);
	}

}
