package dart.blackcat.talker.aot;

public class UnableToAnalyzeException extends RuntimeException {

	private static final long serialVersionUID = -4146791185475680347L;

	public UnableToAnalyzeException() {
		super();
	}

	public UnableToAnalyzeException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnableToAnalyzeException(String message) {
		super(message);
	}

	public UnableToAnalyzeException(Throwable cause) {
		super(cause);
	}

}
